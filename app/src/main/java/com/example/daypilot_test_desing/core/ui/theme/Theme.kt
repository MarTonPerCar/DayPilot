@Composable
fun DayPilotTheme(
    theme: DayPilotTheme = DayPilotTheme.SAGE_GREEN,
    darkMode: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when (theme) {
        DayPilotTheme.SAGE_GREEN -> if (darkMode) SageGreenDarkScheme else SageGreenLightScheme
        DayPilotTheme.OCEAN      -> if (darkMode) OceanDarkScheme     else OceanLightScheme
        DayPilotTheme.LAVENDER   -> if (darkMode) LavenderDarkScheme  else LavenderLightScheme
        DayPilotTheme.AMBER      -> if (darkMode) AmberDarkScheme     else AmberLightScheme
        DayPilotTheme.AMOLED     -> AmoledScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography  = DayPilotTypography,
        content     = content
    )
}