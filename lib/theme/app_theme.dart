import 'package:flutter/material.dart';

enum DayPilotTheme { sageGreen, ocean, lavender, amber, amoled }

final dayPilotThemeNotifier = ValueNotifier<DayPilotTheme>(DayPilotTheme.sageGreen);

final dayPilotThemeModeNotifier = ValueNotifier<ThemeMode>(ThemeMode.system);

ThemeData buildTheme(DayPilotTheme theme, {bool darkMode = false}) {
  return switch (theme) {
    DayPilotTheme.sageGreen => _build(_sageLight, _sageDark, darkMode,
        const Color(0xFFF4F9F5), const Color(0xFF0E1F13)),
    DayPilotTheme.ocean => _build(_oceanLight, _oceanDark, darkMode,
        const Color(0xFFF2F8FB), const Color(0xFF001F2A)),
    DayPilotTheme.lavender => _build(_lavLight, _lavDark, darkMode,
        const Color(0xFFF7F4FC), const Color(0xFF100A20)),
    DayPilotTheme.amber => _build(_amberLight, _amberDark, darkMode,
        const Color(0xFFFFF8F2), const Color(0xFF1F0E00)),
    DayPilotTheme.amoled => _amoledTheme(),
  };
}

ThemeData _build(ColorScheme light, ColorScheme dark, bool darkMode,
    Color bgLight, Color bgDark) {
  final cs = darkMode ? dark : light;
  return ThemeData(
    useMaterial3: true,
    colorScheme: cs,
    scaffoldBackgroundColor: darkMode ? bgDark : bgLight,
  );
}

